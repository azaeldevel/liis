    private void read(Renglon renglon) 
    {
        read(renglon.getRows().get(0).getItem().getItem());
    }
    
    private void read(Item item)
    {
        if(item.getNumber() != null)
        {
            txNumero.setText(item.getNumber());
        }        
        if(item.getCuentaFiscal() != null)
        {
            txCuenta.setText(item.getCuentaFiscal());
        }
        if(item.getDescription() != null)
        {
            txDesc.setText(item.getDescription());
        }
    }

