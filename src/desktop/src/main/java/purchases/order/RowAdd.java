    private void cmbNumberHierarchyChanged(java.awt.event.HierarchyEvent evt) {//GEN-FIRST:event_cmbNumberHierarchyChanged
        NumberComboModel<Item> numberModel = (NumberComboModel<Item>) cmbNumber.getModel();        
        SIIL.core.config.Server serverConfig = new SIIL.core.config.Server();        
        Database dbserver = null;
        try 
        {
            serverConfig.loadFile(new java.io.File(".").getCanonicalPath());
            dbserver = new Database(serverConfig);
        } 
        catch (ClassNotFoundException | SQLException | IOException | ParserConfigurationException | SAXException ex) 
        {
            JOptionPane.showMessageDialog(this,
                "Fallo importacion.",
                "Error Interno",
                JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        
        try
        {
            numberModel.search(dbserver,(String)cmbNumber.getEditor().getItem());
        }
        catch (SQLException ex) 
        {
            Logger.getLogger(RowAdd.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmbNumberHierarchyChanged

