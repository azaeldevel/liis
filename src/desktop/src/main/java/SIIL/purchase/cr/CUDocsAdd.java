    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        //Object[] row = new Object[2];
        if(opMode == ModeOperation.CREATE)
        {
            document = new Document();
        }
        
        if(txPONumber.getText().length() > 0 )
        {
            document.setPO(txPONumber.getText());
            //row[0] = txPONumber.getText();
        }
        if(txSANum.getText().length() > 0)
        {
            document.setSA(txSANum.getText());
            //row[1] = txSANum.getText();
        }
        
        if(txPONumber.getText().length() > 0 || txSANum.getText().length() > 0)
        {
            if(opMode == ModeOperation.CREATE)
            {
                factura.getDocuments().add(document);
            }
            else if(opMode == ModeOperation.EDIT)
            {
                //se actualiza por referencia
            }
            
            cu.loadTable();
            erase();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Nada para agregar","Error externo",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btAddActionPerformed


