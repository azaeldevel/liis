    private void btClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btClientActionPerformed
        comp = new SIIL.Server.Company();
        JFrame frm = new JFrame();
        JDialog dlg = new JDialog(frm,"Seleccionar cliente",true);
        MySQL conn = new MySQL();
        conn.Create();
        if(conn.getConnection() == null)
        {
            JOptionPane.showMessageDialog(this,
                "Conexion a Servidor Invalida",
                "Error Interno",
                JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        SIIL.Clientes.ReadSelect read = new SIIL.Clientes.ReadSelect(comp,BD);
        dlg.setContentPane(read);
        dlg.setSize(460, 260);
        dlg.setVisible(true);
        txClient.setText(comp.getNumber());
        lbClient.setText(comp.getName());
    }//GEN-LAST:event_btClientActionPerformed

    private void txClientKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txClientKeyReleased
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER)
        {
            comp = new SIIL.Server.Company();
            comp.setBD(BD);
            comp.setNumber(txClient.getText());
            SIIL.core.config.Server serverConfig = new SIIL.core.config.Server();        
            Database dbserver = null;
            try 
            {
                serverConfig.loadFile(new java.io.File(".").getCanonicalPath());
                dbserver = new Database(serverConfig);
            } 
            catch (ClassNotFoundException | SQLException | IOException | ParserConfigurationException | SAXException ex) 
            {
                JOptionPane.showMessageDialog(this,
                    "Fallo importacion.",
                    "Error Interno",
                    JOptionPane.ERROR_MESSAGE
                );
                return;
            }
            if(comp.complete(dbserver))
            {
                lbClient.setText(comp.getName());
            }
            else
            {
                if(comp == null)
                {
                    JOptionPane.showMessageDialog(this, "Numero de cliente desconocido",
                        "Error externo", JOptionPane.ERROR_MESSAGE);
                }
                else
                {
                    comp=null;
                    lbClient.setText("###");
                }
            }
            dbserver.close();
        }
    }//GEN-LAST:event_txClientKeyReleased

    private String getWhere() 
    {
        String restrict = "";
        if(txFolio.getText().length() > 0)
        {
            if(restrict.length() > 0)
            {
                restrict = restrict + " AND folio = '" + txFolio.getText() + "'";
            }
            else
            {
                restrict = " folio = '" + txFolio.getText() + "'";
            }
        }
        if(date.getDate() != null)
        {
            SimpleDateFormat sfd = new SimpleDateFormat("yyyy/MM/dd",Locale.ENGLISH);
            if(restrict.length() > 0)
            {
                restrict = restrict + " AND fhmov='" + sfd.format(date.getDate()) + "'";
            }
            else
            {
                restrict = restrict + " fhmov='" + sfd.format(date.getDate()) + "'";
            }
        }
        if(txNumEco.getText().length()>0)
        {
            if(restrict.length() > 0)
            {
                restrict = restrict + " AND numeco = '" + txNumEco.getText() + "'";
            }
            else
            {
                restrict = " numeco = '" + txNumEco.getText() + "'";                
            }
        }
        if(comp != null && txClient.getText().length() > 0)
        {
            if(restrict.length() > 0)
            {
                restrict = restrict + " AND compNumber ='" + comp.getNumber() + "'";
            }
            else
            {
                restrict = " compNumber ='" + comp.getNumber() + "'";
            }
        }
        
        return restrict;
    }


